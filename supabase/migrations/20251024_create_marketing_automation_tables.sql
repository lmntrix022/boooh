-- Function to process pending automation queue items
CREATE OR REPLACE FUNCTION process_automation_queue()
RETURNS TABLE(processed_count INTEGER) AS $$
DECLARE
  processed INTEGER := 0;
  queue_item RECORD;
BEGIN
  -- Get all pending items that should be executed now
  FOR queue_item IN
    SELECT * FROM automation_queue
    WHERE status = 'pending'
    AND execute_at <= NOW()
    ORDER BY execute_at ASC
    LIMIT 100  -- Process in batches
  LOOP
    -- Mark as executing
    UPDATE automation_queue
    SET status = 'executing', updated_at = NOW()
    WHERE id = queue_item.id;

    -- In production, this would trigger actual execution
    -- For now, just mark as completed
    UPDATE automation_queue
    SET status = 'completed',
        executed_at = NOW(),
        updated_at = NOW()
    WHERE id = queue_item.id;

    processed := processed + 1;
  END LOOP;

  RETURN QUERY SELECT processed;
END;
$$ LANGUAGE plpgsql;

-- Function to track RFM segment changes and trigger automation
CREATE OR REPLACE FUNCTION track_rfm_segment_change()
RETURNS TRIGGER AS $$
DECLARE
  previous_segment VARCHAR(50);
  new_segment VARCHAR(50);
BEGIN
  -- Get previous segment from history
  SELECT segment INTO previous_segment
  FROM rfm_segment_history
  WHERE contact_id = NEW.id
  ORDER BY created_at DESC
  LIMIT 1;

  -- Calculate new segment (simplified - actual calculation in app)
  new_segment := NEW.tags[array_position(NEW.tags, 'segment_')];  -- Placeholder

  -- If segment changed, log it
  IF previous_segment IS DISTINCT FROM new_segment THEN
    -- Insert into history
    INSERT INTO rfm_segment_history (contact_id, user_id, segment, r_score, f_score, m_score)
    VALUES (NEW.id, NEW.user_id, new_segment, 0, 0, 0);  -- Scores calculated in app

    -- Trigger automation would happen in application code
    -- This is just the database trigger for logging
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Attach trigger to scanned_contacts table
CREATE TRIGGER track_contact_segment_changes
  AFTER UPDATE ON scanned_contacts
  FOR EACH ROW
  EXECUTE FUNCTION track_rfm_segment_change();

-- Function to calculate automation campaign ROI
CREATE OR REPLACE FUNCTION calculate_campaign_roi(
  p_rule_id VARCHAR(100),
  p_user_id UUID,
  p_period_days INTEGER DEFAULT 30
)
RETURNS TABLE(
  sent_count BIGINT,
  opened_count BIGINT,
  clicked_count BIGINT,
  converted_count BIGINT,
  open_rate NUMERIC,
  click_rate NUMERIC,
  conversion_rate NUMERIC,
  total_revenue NUMERIC,
  estimated_cost NUMERIC,
  roi_percent NUMERIC
) AS $$
DECLARE
  email_cost CONSTANT NUMERIC := 50;  -- 50 FCFA per email
BEGIN
  RETURN QUERY
  WITH campaign_stats AS (
    SELECT
      COUNT(*)::BIGINT as sent,
      COUNT(*) FILTER (WHERE opened_count > 0)::BIGINT as opened,
      COUNT(*) FILTER (WHERE clicked_count > 0)::BIGINT as clicked,
      COUNT(*) FILTER (WHERE converted)::BIGINT as converted,
      COALESCE(SUM(revenue), 0) as revenue
    FROM automation_logs
    WHERE rule_id = p_rule_id
      AND user_id = p_user_id
      AND created_at >= NOW() - INTERVAL '1 day' * p_period_days
  )
  SELECT
    sent,
    opened,
    clicked,
    converted,
    CASE WHEN sent > 0 THEN ROUND((opened::NUMERIC / sent::NUMERIC) * 100, 2) ELSE 0 END as open_rate,
    CASE WHEN sent > 0 THEN ROUND((clicked::NUMERIC / sent::NUMERIC) * 100, 2) ELSE 0 END as click_rate,
    CASE WHEN sent > 0 THEN ROUND((converted::NUMERIC / sent::NUMERIC) * 100, 2) ELSE 0 END as conversion_rate,
    revenue,
    sent * email_cost as cost,
    CASE WHEN sent * email_cost > 0
      THEN ROUND(((revenue - (sent * email_cost)) / (sent * email_cost)) * 100, 2)
      ELSE 0
    END as roi
  FROM campaign_stats;
END;
$$ LANGUAGE plpgsql;

