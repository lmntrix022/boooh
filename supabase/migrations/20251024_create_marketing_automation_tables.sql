-- Marketing Automation Tables
-- Support for automated campaigns based on RFM segmentation

-- ============================================================================
-- automation_queue: Scheduled automation actions
-- ============================================================================

CREATE TABLE IF NOT EXISTS automation_queue (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  rule_id VARCHAR(100) NOT NULL,
  contact_id UUID NOT NULL REFERENCES scanned_contacts(id) ON DELETE CASCADE,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,

  action_type VARCHAR(50) NOT NULL,
  action_data JSONB NOT NULL DEFAULT '{}',

  execute_at TIMESTAMP WITH TIME ZONE NOT NULL,
  status VARCHAR(20) NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'executing', 'completed', 'failed', 'cancelled')),

  executed_at TIMESTAMP WITH TIME ZONE,
  error TEXT,

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Indexes for performance
CREATE INDEX idx_automation_queue_execute_at ON automation_queue(execute_at) WHERE status = 'pending';
CREATE INDEX idx_automation_queue_contact_id ON automation_queue(contact_id);
CREATE INDEX idx_automation_queue_user_id ON automation_queue(user_id);
CREATE INDEX idx_automation_queue_status ON automation_queue(status);
CREATE INDEX idx_automation_queue_rule_id ON automation_queue(rule_id);

-- ============================================================================
-- automation_logs: Execution history
-- ============================================================================

CREATE TABLE IF NOT EXISTS automation_logs (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  rule_id VARCHAR(100) NOT NULL,
  contact_id UUID NOT NULL REFERENCES scanned_contacts(id) ON DELETE CASCADE,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,

  status VARCHAR(20) NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'executing', 'completed', 'failed')),
  actions JSONB NOT NULL DEFAULT '[]',

  error TEXT,

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  completed_at TIMESTAMP WITH TIME ZONE,

  -- Metrics
  sent_count INTEGER DEFAULT 0,
  opened_count INTEGER DEFAULT 0,
  clicked_count INTEGER DEFAULT 0,
  converted BOOLEAN DEFAULT false,
  revenue NUMERIC(10, 2) DEFAULT 0
);

-- Indexes
CREATE INDEX idx_automation_logs_rule_id ON automation_logs(rule_id);
CREATE INDEX idx_automation_logs_contact_id ON automation_logs(contact_id);
CREATE INDEX idx_automation_logs_user_id ON automation_logs(user_id);
CREATE INDEX idx_automation_logs_created_at ON automation_logs(created_at DESC);
CREATE INDEX idx_automation_logs_status ON automation_logs(status);

-- ============================================================================
-- crm_tasks: Tasks generated by automation
-- ============================================================================

CREATE TABLE IF NOT EXISTS crm_tasks (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  contact_id UUID NOT NULL REFERENCES scanned_contacts(id) ON DELETE CASCADE,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  assigned_to UUID REFERENCES auth.users(id) ON DELETE SET NULL,

  task_type VARCHAR(50) NOT NULL CHECK (task_type IN ('call', 'email', 'meeting', 'follow_up', 'assignment', 'general')),
  priority VARCHAR(20) NOT NULL DEFAULT 'medium' CHECK (priority IN ('low', 'medium', 'high', 'critical')),

  title TEXT NOT NULL,
  description TEXT,

  status VARCHAR(20) NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'in_progress', 'completed', 'cancelled')),

  due_date TIMESTAMP WITH TIME ZONE,
  completed_at TIMESTAMP WITH TIME ZONE,

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

  -- Metadata
  metadata JSONB DEFAULT '{}'
);

-- Indexes
CREATE INDEX idx_crm_tasks_contact_id ON crm_tasks(contact_id);
CREATE INDEX idx_crm_tasks_user_id ON crm_tasks(user_id);
CREATE INDEX idx_crm_tasks_assigned_to ON crm_tasks(assigned_to);
CREATE INDEX idx_crm_tasks_status ON crm_tasks(status);
CREATE INDEX idx_crm_tasks_priority ON crm_tasks(priority);
CREATE INDEX idx_crm_tasks_due_date ON crm_tasks(due_date) WHERE status != 'completed';

-- ============================================================================
-- automation_analytics: Campaign performance metrics
-- ============================================================================

CREATE TABLE IF NOT EXISTS automation_analytics (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  rule_id VARCHAR(100) NOT NULL,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,

  period_start DATE NOT NULL,
  period_end DATE NOT NULL,

  -- Campaign metrics
  sent_count INTEGER DEFAULT 0,
  delivered_count INTEGER DEFAULT 0,
  opened_count INTEGER DEFAULT 0,
  clicked_count INTEGER DEFAULT 0,
  converted_count INTEGER DEFAULT 0,

  -- Rates (calculated)
  open_rate NUMERIC(5, 4) DEFAULT 0,
  click_rate NUMERIC(5, 4) DEFAULT 0,
  conversion_rate NUMERIC(5, 4) DEFAULT 0,

  -- Revenue
  total_revenue NUMERIC(12, 2) DEFAULT 0,

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Indexes
CREATE INDEX idx_automation_analytics_rule_id ON automation_analytics(rule_id);
CREATE INDEX idx_automation_analytics_user_id ON automation_analytics(user_id);
CREATE INDEX idx_automation_analytics_period ON automation_analytics(period_start, period_end);

-- Unique constraint for one record per rule per period
CREATE UNIQUE INDEX idx_automation_analytics_unique ON automation_analytics(rule_id, user_id, period_start);

-- ============================================================================
-- RFM segment history: Track segment changes over time
-- ============================================================================

CREATE TABLE IF NOT EXISTS rfm_segment_history (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  contact_id UUID NOT NULL REFERENCES scanned_contacts(id) ON DELETE CASCADE,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,

  segment VARCHAR(50) NOT NULL,
  r_score INTEGER NOT NULL CHECK (r_score >= 1 AND r_score <= 5),
  f_score INTEGER NOT NULL CHECK (f_score >= 1 AND f_score <= 5),
  m_score INTEGER NOT NULL CHECK (m_score >= 1 AND m_score <= 5),

  -- Stats at time of segment calculation
  total_revenue NUMERIC(12, 2),
  total_orders INTEGER,
  days_since_last_order INTEGER,

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Indexes
CREATE INDEX idx_rfm_history_contact_id ON rfm_segment_history(contact_id);
CREATE INDEX idx_rfm_history_user_id ON rfm_segment_history(user_id);
CREATE INDEX idx_rfm_history_segment ON rfm_segment_history(segment);
CREATE INDEX idx_rfm_history_created_at ON rfm_segment_history(created_at DESC);

